#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*mem1;
	unsigned char	*mem2;
	size_t			i;

	i = 0;
	mem1 = (unsigned char *)s1;
	mem2 = (unsigned char *)s2;
	while (n--)
	{
		if (*mem1 != *mem2)
			return ((unsigned int)(*mem1 - *mem2));
		if (n)
		{
			mem1++;
			mem2++;
		}
	}
	return (0);
}
